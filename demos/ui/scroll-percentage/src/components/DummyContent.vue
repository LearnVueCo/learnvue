      Vue uses an HTML-based template syntax that allows binding the rendered
      DOM to the underlying Vue instance's data. All Vue templates are valid
      HTML that can be parsed by specification-compliant browsers and HTML
      parsers. Vue compiles the templates into virtual DOM render functions. A
      virtual Document Object Model (or "DOM") allows Vue to render components
      in its memory before updating the browser. Combined with the reactivity
      system, Vue can calculate the minimal number of components to re-render
      and apply the minimal amount of DOM manipulations when the app state
      changes.
    </p>

