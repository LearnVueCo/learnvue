    <h2>Single Page Applications</h2>
    <p>
      A traditional disadvantage of single-page applications (SPAs) is the
      inability to share links to the exact "sub" page within a specific web
      page. Because SPAs serve their users only one URL-based response from the
      server (it typically serves index.html or index.vue), bookmarking certain
      screens or sharing links to specific sections is normally difficult if not
      impossible. To solve this problem, many client-side routers delimit their
      dynamic URLs with a "hashbang" (#!), e.g. page.com/#!/. However, with
      HTML5 most modern browsers support routing without hashbangs.
